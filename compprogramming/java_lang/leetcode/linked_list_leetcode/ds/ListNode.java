package java_lang.linked_list_leetcode.ds;

public class ListNode {
    //private requires getters and setters
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
